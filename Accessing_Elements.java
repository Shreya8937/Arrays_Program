public class Accessing_Elements {
	
	    public static void main(String[] args) {
	        int[] array = {1, 2, 3, 4, 5};
	        
	        // Accessing the first element
	        int firstElement = array[1];
	        System.out.println("The first element is: " + firstElement);
	        
	        // Accessing the fourth element
	        int fourthElement = array[4];
	        System.out.println("The fourth element is: " + fourthElement);
	    }
	}
